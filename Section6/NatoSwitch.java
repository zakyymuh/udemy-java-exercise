package Section6;

public class NatoSwitch {
  public static void main(String[] args) {
    char charValue = 'F';
    switch (charValue) {
      case 'A' :
        System.out.println("A is Adang");
        break;
      case 'B' :
        System.out.println("B is Budi");
        break;
      case 'C' :
        System.out.println("C is Caca");
        break;
      default :
        System.out.println("Found Nothing");
        break;
    }
  }

  
}
